#!/bin/bash
#Use ncap2 and ncks to add AQMEII2 recommended variables and remove individual ART outputs
#Log: P. Shrestha (Sep 2. 2021)
#
sdir=/p/largedata/hbn33/shrestha1/HET_ART_20150705/runART_8_HET/cosout
for entry in "$search_dir"${sdir}/*.nc
do
  echo "Add AQMEII2 recommended variables to and reduce " $entry
  ncap2 -A -S aqmeii2.nco $entry temp.nc
  ncks -x -v ETH,HC3,HC5,HC8,OL2,OLT,OLI,TOL,XYL,CSL,HCHO,ALD,KET,ISO,GLY,MGLY,DCB,MACR,API,LIM,ONIT,ORA1,ORA2,VSO4J,VSO4I,VSO4Jm,VSO4Im,VSO4SEASA,VSO4SEASB,VSO4SEASC,VNO3J,VNO3I,VNO3Jm,VNO3Im,VNH4J,VNH4I,VNH4Jm,VNH4Im,VSOOTJ,VSOOTI,VSOOT,VORGPAJ,VORGPAI,VORGPAJm,VORGPAIm,VP25AI,VP25AJ,VORG1J,VORG1I,VORG10J,VORG10I,VORG100J,VORG100I,VORG1000J,VORG1000I,VORG1Jm,VORG1Im,VORG10Jm,VORG10Im,VORG100Jm,VORG100Im,VORG1000Jm,VORG1000Im,VSOILA,VSOILB,VSOILC,VSEASA,VSEASB,VSEASC,VANTHA,VNU0,VAC0,VNU0m,VAC0m,VSOOT0,VCORN,VSOILA0,VSOILB0,VSOILC0,VSEASA0,VSEASB0,VSEASC0  temp.nc out.nc
  mv out.nc $entry
  rm temp.nc
done
exit

